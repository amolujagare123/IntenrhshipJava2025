package loops;

public class incrementdec1 {

    public static void main(String[] args) {

        int i =10;

        System.out.println(i); // 10
        System.out.println(i++); // 10
        System.out.println(i); // 11
        System.out.println(++i); // 12
        System.out.println(i); // 12

      /*  a++ , a - - : First the statement is executed
      then the increment or decrement happens
     ++a , - - a :  first increment or recruitment
     happens then the statements are executed
*/
    }
}
